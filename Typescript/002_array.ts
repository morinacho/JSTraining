// Array y tipados
// Array de tipo number
let numerosArray : number[] = [1,2,3,4,5];

// Array de tipo string
let letras : string[] = ['a','b','c','d','e'];

// Array de tipo boolean
let verdadero_falsoArray : boolean[] = [true,false,true,false,true];

// Array de tipo any
let cualquierTipo : any[] = [1,'a',true,false,{nombre:'Juan'}];

//Otra forma de declarar un array
let coleccionNumero : Array<number> = [1,2,3,4,5];
let coleccionLetras : Array<string> = ['a','b','c','d','e'];